#include <iostream>

using namespace std;

int main () {
	
	int contador = 0;
	contador++; // contador += 1; contador = contador + 1;
	contador--; // contador -= 1; contador = contador - 1;
	
	// *=   /=
	
	contador++; // Pós incremento - usa o valor e depois incrementa
	++contador; // Pré decremento - incrementa e depois usa o valor
	
	return 0;
	
}
